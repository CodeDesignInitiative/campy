<div class="text-blue-darker leading-normal"><h2 id="toc_2">Einwilligung der Speicherung personenbezogener Daten</h2>

<ul>
<li><p>Kurzform: Wir speichern und verarbeiten nur, was wir für die Camp-Durchführung und steuerrechtlich benötigen</p></li>
<li><p><em>Warum?</em> Zur Vorbereitung, Durchführung und rechtssicheren Dokumentation des Camps benötigen wir personenbezogene Daten</p></li>
<li><p><em>Welche Daten?</em> Wir speichern folgende Daten: Vorname, Nachname, Email-Adresse, Geburtsdatum, Ernährungsweise, PLZ</p></li>
<li><p><em>Welche zusätzlichen Daten bei Minderjährigen?</em> Bei Minderjährigen erheben wir zusätzlich Vorname, Nachname, Email-Adresse und Telefonnummer eines Erziehungsberechtigten</p></li>
<li><p><em>Welche Daten nicht?</em> Wir erheben keine Angaben zum Geschlecht</p></li>
<li><p><em>Wofür?</em></p>

<ul>
<li><em>Vorname und Nachname:</em> Zurordnung von Zahlungsvorgängen</li>
<li><em>Email-Adresse:</em> Versand von Informationen zum Camp, ggf. Hinweis auf weitere Camps in der Nähe in der Zukunft</li>
<li><em>Geburtsdatum:</em> Feststellung, ob Teilnehmer Bestätigung der Erziehungsberechtigten benötigt</li>
<li><em>Ernährungsweise:</em> Zur Bestellung des Caterings ist es notwendig, die Anzahl an Sonderdiäten zu kennen</li>
<li><em>PLZ:</em> Benötigt für kommunal gebundene Förderung (etwa durch die Arbeitsagentur oder Städte)</li>
<li><em>Daten der Erziehungsberechtigten:</em> Kontaktaufnahme a, zur Bestätigung der Einwilligung und Teilnahmebedingungen und b, bei Problemen während der Campzeit</li>
</ul></li>
<li><p><em>Was passiert nach dem Camp mit den Daten?</em></p></li>
<li><p><em>Werden die Personendaten mit Dritten geteilt?</em> </p>

<ul>
<li>Nein, sie sind nur den dafür berechtigten Personen bei der Code+Design Initiative e.V. zugänglich.</li>
<li>Anonymisierte statistische Werte werden an Partner weitergegeben, z.B. die Teilnehmeranzahl</li>
<li>Steuerrechtlich sind wir verpflichtet, personenbezogene Daten 10 Jahre zu speichern, die mit steuerpflichtigen Umsätzen verbunden sind. Dies sind: Vorname, Nachname, Geburtstag</li>
<li>Die weiteren Daten (Email-Adresse, Ernährungsweise, PLZ) werden nicht automatisch gelöscht, da sie für eine weitere Camp-Anmeldung genutzt werden können. Die Daten können im persönlichen Profil aber jederzeit selbst geändert oder gelöscht werden</li>
</ul></li>
<li><p><em>Was ist, wenn ich der Erhebung nicht zustimme oder sie widerrufe?</em> Widerruf ist jederzeit möglich. Ebenso wie bei Ablehnung der Erhebung ist eine Teilnahme am Camp aber nicht möglich, weil die Durchführung ohne Personendaten nicht möglich ist</p></li>
</ul></div>